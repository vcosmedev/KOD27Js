// 1- Crear funciones que hagan lo siguiente:

// - Sumar dos números 
// - Restar dos números 
// - Multiplicar dos números 
// - Dividir dos números

function numbersAddition(a, b) {
    let result = a + b;
    return result;
}
console.log(numbersAddition(4,4));

function numbersSubstraction(a,b) {
    let result = a - b;
    return result;
}
console.log(numbersSubstraction(10,4));

function numbersMultiplication(a, b) {
    return a * b;
}
console.log(numbersMultiplication(7, 8));

function numbersDivision(a, b) {
    let result = a / b;
    return result;
}
console.log(numbersDivision(10, 2));
